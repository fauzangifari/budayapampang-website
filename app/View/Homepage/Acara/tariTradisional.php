                            <h3>Tari Bahau</h3>
                            <p>Tari Bahau adalah tarian yang menunjukkan kekuatan dan ketangguhan pria Dayak dalam menghadapi tantangan hidup. Gerakan tarian ini penuh dengan semangat dan kekuatan.</p>
