                            <p>Tari Leleng adalah tarian yang menceritakan kisah seorang gadis Dayak yang melarikan diri dari perjodohan yang tidak diinginkannya. Tarian ini penuh dengan ekspresi emosional dan gerakan yang indah.</p>

                            <h3>Tari Ngelawai</h3>
