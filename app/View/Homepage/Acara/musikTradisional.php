                            <p>Desa Budaya Pampang adalah rumah bagi suku Dayak Kenyah yang kaya akan warisan budaya, termasuk musik tradisional. Berikut adalah aktivitas yang bisa dinikmati dalam acara musik tradisional di Wisata Budaya Pampang:</p>

                            <h3>Sape</h3>
                            <p>Sape adalah alat musik tradisional khas Dayak yang dimainkan dengan cara dipetik. Alunan musiknya lembut dan melodius, sering digunakan untuk mengiringi tarian dan upacara adat.</p>

                            <h3>Sampe</h3>
                            <p>Sampe adalah versi yang lebih kecil dari Sape dan sering dimainkan dalam ansambel. Alat musik ini memberikan nada yang tinggi dan ceria, menambah harmoni pada musik tradisional Dayak.</p>

                            <h3>Gamelan Dayak</h3>
                            <p>Gamelan Dayak terdiri dari berbagai instrumen seperti gong, kenong, dan drum. Musik gamelan ini dimainkan dalam upacara adat dan pertunjukan seni, menciptakan suasana yang sakral dan meriah.</p>

                            <h3>Karungut</h3>
                            <p>Karungut adalah bentuk musik vokal yang dinyanyikan dengan iringan alat musik tradisional. Liriknya sering kali menceritakan kisah-kisah legenda dan nasihat kehidupan.</p>

                            <h3>Kelentangan</h3>
                            <p>Kelentangan adalah jenis musik yang menggunakan alat musik perunggu seperti gong kecil. Musik ini sering dimainkan dalam upacara penyambutan tamu dan acara adat lainnya.</p>

                            <h3>Gong</h3>
                            <p>Gong adalah alat musik yang penting dalam setiap upacara adat Dayak. Bunyi gong yang dalam dan bergaung menandai dimulainya atau berakhirnya sebuah acara penting.</p>

                            <h3>Kendang</h3>
                            <p>Kendang adalah drum tradisional yang digunakan dalam berbagai musik dan tarian Dayak. Ritme kendang yang dinamis dan bervariasi memberikan dasar yang kuat bagi komposisi musik tradisional.</p>

                            <h3>Balikan</h3>
                            <p>Balikan adalah alat musik tradisional yang dimainkan dengan cara dipukul. Instrumen ini menghasilkan suara yang unik dan digunakan dalam ansambel musik tradisional Dayak.</p>

                            <h3>Seruling</h3>
                            <p>Seruling Dayak terbuat dari bambu dan dimainkan dengan cara ditiup. Nada seruling yang merdu sering kali mengiringi lagu-lagu dan tarian tradisional.</p>

                            <h3>Jatung Utang</h3>
                            <p>Jatung Utang adalah alat musik petik tradisional yang terbuat dari kayu dan senar. Musik yang dihasilkan berfungsi sebagai hiburan dan pengiring upacara adat.</p>
